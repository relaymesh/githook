package oauth

import (
	"net/http"
	"testing"

	"github.com/relaymesh/githook/pkg/auth"
)

type oauthProviderStub struct {
	name string
}

func (o oauthProviderStub) Name() string         { return o.name }
func (o oauthProviderStub) CallbackPath() string { return "/auth/" + o.name + "/callback" }
func (o oauthProviderStub) AuthorizeURL(r *http.Request, cfg auth.ProviderConfig, state, endpoint string) (string, error) {
	return endpoint + "/" + o.name + "?state=" + state, nil
}
func (o oauthProviderStub) NewHandler(cfg auth.ProviderConfig, opts HandlerOptions) http.Handler {
	return http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) { w.WriteHeader(http.StatusNoContent) })
}

func TestOAuthRegistry(t *testing.T) {
	var nilRegistry *Registry
	if _, ok := nilRegistry.Provider("github"); ok {
		t.Fatalf("expected nil registry lookup false")
	}
	if providers := nilRegistry.Providers(); providers != nil {
		t.Fatalf("expected nil providers for nil registry")
	}

	r := NewRegistry()
	if err := r.Register(nil); err == nil {
		t.Fatalf("expected nil provider error")
	}
	if err := r.Register(oauthProviderStub{name: ""}); err == nil {
		t.Fatalf("expected provider name error")
	}
	if err := r.Register(oauthProviderStub{name: "gitlab"}); err != nil {
		t.Fatalf("register provider: %v", err)
	}
	if err := r.Register(oauthProviderStub{name: "gitlab"}); err == nil {
		t.Fatalf("expected duplicate provider error")
	}
	if _, ok := r.Provider("gitlab"); !ok {
		t.Fatalf("expected provider lookup to succeed")
	}

	if len(r.Providers()) != 1 {
		t.Fatalf("expected one provider")
	}

	var bad *Registry
	if err := bad.Register(oauthProviderStub{name: "x"}); err == nil {
		t.Fatalf("expected nil registry register error")
	}
}

func TestOAuthDefaultRegistry(t *testing.T) {
	r := DefaultRegistry()
	if _, ok := r.Provider("github"); !ok {
		t.Fatalf("expected github provider")
	}
	if _, ok := r.Provider("gitlab"); !ok {
		t.Fatalf("expected gitlab provider")
	}
	if _, ok := r.Provider("bitbucket"); !ok {
		t.Fatalf("expected bitbucket provider")
	}
}
